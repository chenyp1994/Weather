package com.chenyp.jpzjweather.bean;

public class Weather {

	private String date;
	private String maxTemp;
	private String minTemp;
	private String dayWeatherType;
	private String dayWindD;
	private String dayWindS;
	private String dayImg;
	private String nightWeatherType;
	private String nightWindD;
	private String nightWindS;
	private String nightImg;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getMaxTemp() {
		return maxTemp;
	}

	public void setMaxTemp(String maxTemp) {
		this.maxTemp = maxTemp;
	}

	public String getMinTemp() {
		return minTemp;
	}

	public void setMinTemp(String minTemp) {
		this.minTemp = minTemp;
	}

	public String getDayWeatherType() {
		return dayWeatherType;
	}

	public void setDayWeatherType(String dayWeatherType) {
		this.dayWeatherType = dayWeatherType;
	}

	public String getDayWindD() {
		return dayWindD;
	}

	public void setDayWindD(String dayWindD) {
		this.dayWindD = dayWindD;
	}

	public String getDayWindS() {
		return dayWindS;
	}

	public void setDayWindS(String dayWindS) {
		this.dayWindS = dayWindS;
	}

	public String getDayImg() {
		return dayImg;
	}

	public void setDayImg(String dayImg) {
		this.dayImg = dayImg;
	}

	public String getNightWeatherType() {
		return nightWeatherType;
	}

	public void setNightWeatherType(String nightWeatherType) {
		this.nightWeatherType = nightWeatherType;
	}

	public String getNightWindD() {
		return nightWindD;
	}

	public void setNightWindD(String nightWindD) {
		this.nightWindD = nightWindD;
	}

	public String getNightWindS() {
		return nightWindS;
	}

	public void setNightWindS(String nightWindS) {
		this.nightWindS = nightWindS;
	}

	public String getNightImg() {
		return nightImg;
	}

	public void setNightImg(String nightImg) {
		this.nightImg = nightImg;
	}

}
